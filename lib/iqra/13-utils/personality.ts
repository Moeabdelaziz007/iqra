// ── Detect language from input ────────────────────────────────────────────────

export function detectLanguage(input: string): 'ar' | 'en' {
  const arabicChars = (input.match(/[\u0600-\u06FF]/g) || []).length;
  const totalChars = input.replace(/\s/g, '').length;
  return arabicChars / totalChars > 0.3 ? 'ar' : 'en';
}

// ── Format IQRA response with personality ────────────────────────────────────

export function addPersonalityLayer(response: string, lang: 'ar' | 'en'): string {
  // Add "والله أعلم" for uncertain responses
  const uncertaintyMarkers = ['i think', 'possibly', 'might be', 'not sure', 'ربما', 'أظن', 'قد يكون'];
  const hasUncertainty = uncertaintyMarkers.some(m => response.toLowerCase().includes(m));

  let finalResponse = response;

  if (hasUncertainty) {
    const suffix = lang === 'ar' ? '\n\nوالله أعلم وأحكم.' : '\n\nAnd Allah knows best.';
    if (!finalResponse.includes('والله أعلم') && !finalResponse.includes('Allah knows best')) {
      finalResponse += suffix;
    }
  }

  // Inject symbols and granular persona details
  // 1. Add ﷺ after mentioning the Prophet
  finalResponse = finalResponse.replace(/(النبي|الرسول|محمد|Prophet|Messenger|Muhammad)(?!\s*ﷺ)/g, '$1 ﷺ');

  // 2. Add 📖 before Quranic references if not present
  finalResponse = finalResponse.replace(/(?<!📖\s*)(سورة|سورة\s+\w+|Quran|Surah\s+\d+)/g, '📖 $1');

  // 3. Add 🌿 before Hadith references
  finalResponse = finalResponse.replace(/(?<!🌿\s*)(حديث|الحديث|Hadith)/g, '🌿 $1');

  // 4. Add 🕌 before sermons/counsels (simple heuristic)
  if (finalResponse.includes('أوصيك') || finalResponse.includes('نصيحة') || finalResponse.includes('advice') || finalResponse.includes('counsel')) {
    finalResponse = '🕌 ' + finalResponse;
  }

  // 5. Use "..." for reflection/pauses if long
  if (finalResponse.length > 300 && !finalResponse.includes('...')) {
     const parts = finalResponse.split('\n\n');
     if (parts.length > 1) {
       parts[0] = parts[0] + ' ...';
       finalResponse = parts.join('\n\n');
     }
  }

  return finalResponse;
}
